package cams.utils;

/**
 * Utility class that stores the value that should be used by the user input to dismiss / exit out of a view
 */
public class Dismiss {

    /**
     * string value for dismissing / exiting a view
     * @return string value for dismissing / exiting a view
     */
    public static String stringOption() {
        return "-1";
    }

    /**
     * int value for dismissing / exiting a view
     * @return int value for dismissing / exiting a view
     */
    public static int intOption() {
        return -1;
    }
}
